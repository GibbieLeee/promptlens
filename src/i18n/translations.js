// Функция для получения перевода с подстановкой переменных
export function t(key, lang = "en", params = {}) {
  const translation = translations[lang]?.[key] || translations["en"][key] || key;
  
  // Проверяем, что translation - строка
  if (typeof translation !== "string") {
    console.warn(`Translation for key "${key}" in language "${lang}" is not a string:`, translation);
    return String(translation || key);
  }
  
  // Замена переменных вида {variable}
  return translation.replace(/\{(\w+)\}/g, (match, paramKey) => {
    return params[paramKey] !== undefined ? params[paramKey] : match;
  });
}

